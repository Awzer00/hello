package com.project.entity;

import java.io.Serializable;
/**
 * 
 * role 实体类
 * Fri May 04 17:08:39 CST 2018
 * @author 白明 
 * 
 */ 
public class Role implements Serializable{
	private int id;
	private String roleName;
	private String logId;
	private String logPwd;
	private String rlimit;

	public Role(){
		super();
	}

	public Role(int id,String roleName,String logId,String logPwd,String rlimit){
		super();
		this.id=id;
		this.roleName=roleName;
		this.logId=logId;
		this.logPwd=logPwd;
		this.rlimit=rlimit;
	}

	@Override
	public String toString(){
		return "Role [id=" + id + ", roleName=" + roleName + ", logId=" + logId + ", logPwd=" + logPwd + ", rlimit=" + rlimit + "]";
	}

	public void setId(int id){
		this.id=id;
	}

	public int getId(){
		return id;
	}

	public void setRoleName(String roleName){
		this.roleName=roleName;
	}

	public String getRoleName(){
		return roleName;
	}

	public void setLogId(String logId){
		this.logId=logId;
	}

	public String getLogId(){
		return logId;
	}

	public void setLogPwd(String logPwd){
		this.logPwd=logPwd;
	}

	public String getLogPwd(){
		return logPwd;
	}

	public void setRlimit(String rlimit){
		this.rlimit=rlimit;
	}

	public String getRlimit(){
		return rlimit;
	}
}

